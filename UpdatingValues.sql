102,121,156,102 for 123,114,115,121 for 127.

UPDATE EMPLOYEE
SET MANAGER_ID = NULL
WHERE ID = 101;

UPDATE EMPLOYEE
SET MANAGER_ID = 101
WHERE ID = 102 OR ID=121 OR ID=156;

UPDATE EMPLOYEE
SET MANAGER_ID = 102
WHERE ID = 123 OR ID=114 OR ID=115;

UPDATE EMPLOYEE
SET MANAGER_ID = 121
WHERE ID = 127;